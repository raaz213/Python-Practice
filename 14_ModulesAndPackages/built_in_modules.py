import math
print(math.sqrt(16))     # 4.0
print(math.pow(5,2))     # 25.0
print(math.ceil(4.5))     # 5
print(math.floor(4.5))     # 4
print(f"{math.sin(45):.2f}") # 0.85
print(f"{math.pi:.2f}")     # 3.14

import random
print(random.randint(1, 10))  # Random number between 1-10
print(random.random())        # Random float between 0-1

